# Fundamental Data Type
# int = Integer or whole numbers
print(type(2 + 4))  # 6
print(type(2 - 4))  # -2
print(type(2 * 4))  # 8
print(type(2 / 4))  # 0.5

# add an int and float will return float
print(type(20 + 1.1))

print(type(9.9 + 1.1))

## double or expondental
print(2 ** 3)

# this will round down to int
print(5 // 4)

# modulal - reminder of the division
print(5 % 4)
